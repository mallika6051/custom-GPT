# join function
names=['Mallika', 'Meena', 'Hariharan', 'Viji', 'Swetha']
print(":".join(names))
# Replace Function
names='Mallika, Meena, Hariharan, Viji, Swetha'
print(names.replace('Mallika','Dharshini'))
# startswith function
print(names.startswith('Mallika'))
print(names.startswith('Meena'))
# endswith function
print(names.endswith('Swetha'))
print(names.endswith('Mallika'))
# upper function and lower function
print(names.upper())
print(names.lower())
